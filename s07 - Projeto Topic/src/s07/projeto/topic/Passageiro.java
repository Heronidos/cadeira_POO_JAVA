package s07.projeto.topic;
public class Passageiro {
    private String idNome;
    private int idade;

    public Passageiro(String idNome, int idade) {
        this.idNome = idNome;
        this.idade = idade;
    }

    public String getIdNome() {
        return idNome;
    }

    public void setIdNome(String idNome) {
        this.idNome = idNome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }
    
    
    
}
